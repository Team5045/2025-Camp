public class Booleans {
    public static void main(String[] args){
        // Booleans store true and false values.
        boolean amIExcitedForSummerCamp = true;
        boolean amIExcitedForSchool = false;

        System.out.println(!amIExcitedForSummerCamp); // false

        System.out.println(amIExcitedForSchool && amIExcitedForSummerCamp); // false

        System.out.println(amIExcitedForSummerCamp || amIExcitedForSchool); // true
    }
}
